#RANGE 

print('\n\t\tRANGE\t\t\n')

									
for x in range(10):							# to make an action ('n') times
	print("Pippo")							# ...

print('')

for x in range(5, 12):						# second example -->  (from 'x' to(,) 'y') 
	print(x)								# ...

print('')

for x in range(10, 40, 5):					# third example -->  (from 'x' to(,) 'y' in steps of(,) 'z') 
	print(x)								# ...

print('')